namespace Bee.Definition
{
    /// <summary>
    /// Control type.
    /// </summary>
    public enum ControlType
    {
        /// <summary>
        /// Automatically determined.
        /// </summary>
        Auto,
        /// <summary>
        /// Text edit box.
        /// </summary>
        TextEdit,
        /// <summary>
        /// Button edit box.
        /// </summary>
        ButtonEdit,
        /// <summary>
        /// Date input box.
        /// </summary>
        DateEdit,
        /// <summary>
        /// Year-month input box.
        /// </summary>
        YearMonthEdit,
        /// <summary>
        /// Drop-down list.
        /// </summary>
        DropDownEdit,
        /// <summary>
        /// Memo (multi-line text) input box.
        /// </summary>
        MemoEdit,
        /// <summary>
        /// Check box.
        /// </summary>
        CheckEdit
    }
}
