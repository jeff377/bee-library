using System;
using Bee.Api.Contracts;
using MessagePack;

namespace Bee.Api.Core.System
{
    /// <summary>
    /// API request for the login operation.
    /// </summary>
    [MessagePackObject]
    [Serializable]
    public class LoginRequest : ApiRequest, ILoginRequest
    {
        /// <summary>
        /// Gets or sets the user account identifier.
        /// </summary>
        [Key(100)]
        public string UserId { get; set; } = string.Empty;

        /// <summary>
        /// Gets or sets the password (plain text or pre-encrypted).
        /// </summary>
        [Key(101)]
        public string Password { get; set; } = string.Empty;

        /// <summary>
        /// Gets or sets the RSA public key generated by the client for key exchange.
        /// </summary>
        [Key(102)]
        public string ClientPublicKey { get; set; } = string.Empty;
    }
}
